// Librería de entrada y salida
#include <iostream>

// Usamos el espacio de nombres estándar
using namespace std;

// Función principal main
int main(){

    // Comentarios en C++ pueden ser:
    // - De una sola línea con //
    // - De varias líneas con /* ... */

    /* 
        Ejemplo de comentario
        en varias líneas
    */

    // cout: videne del  "character output stream" (salida de caracteres).
    // Sirve para mostrar información en la consola.
    // Se usa con el operador << para enviar datos al flujo de salida.
    // endl: significa "end line" (fin de línea) es decir un salto de linea.


    cout << endl << "Hello World! la fecha en que fue escrita esta linea fue a las 6:51 pm del 05/09/";
    cout << 2025 << endl;

    // Ejemplo con frases en diferentes líneas
    cout << endl << "Hola " << endl;
    cout << "Mundo" << endl;

    return 0; // opcional en C++
}
